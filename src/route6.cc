/**
 * @brief Convert IPv6 mask in CIDR notation to bytes array.
 * 
 * @param src IPv6 mask in CIDR notation.
 * @param dst Bytes array to write to.
 * @return true Converted.
 * @return false Failed to convert.
 */
bool cidr_to_mask6(uint8_t src, uint8_t dst[16]) {
    if (src > 128) return false;
    memcpy(dst, CIDR_MASK_MAP6[src], 16);
    return true;
}

/**
 * @brief Mask a IPv6 address with given netmask.
 * 
 * @param prefix Prefix to mask.
 * @param mask IPv6 mask in CIDR notation.
 * @param masked_addr Bytes array to store masked address.
 * @return true Masked.
 * @return false Failed to mask.
 */
bool mask_ipv6(const uint8_t prefix[16], uint8_t mask, uint8_t masked_addr[16]) {
    if (mask > 128) return false;
    const uint8_t* mask_arr = CIDR_MASK_MAP6[mask];

    for (int i = 0; i < 16; i++) {
        masked_addr[i] = prefix[i] & mask_arr[i];
    }

    return true;
}

/**
 * @brief Test if an address is inside a prefix.
 * 
 * @param prefix The prefix.
 * @param length The netmask of prefix in CIDR notation.
 * @param address The address to test against.
 * @return true The address is in the prefix.
 * @return false The address in not in the prefix.
 */
bool Route6::Includes (const uint8_t prefix[16], uint8_t length, const uint8_t address[16]) {
    if (length > 128) return false;

    uint8_t masked_address[16];
    if (!mask_ipv6(address, length, masked_address)) return false;

    return memcmp(prefix, masked_address, 16) == 0;
}

